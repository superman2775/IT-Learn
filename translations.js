// Language handling functions
function updatePageLanguage(lang) {
    document.documentElement.lang = lang;
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.dataset.translate;
        const translation = translations[lang]?.[key];
        if (translation) {
            element.innerHTML = translation;
        }
    });
}


// Language selector functionality
function initializeLanguageSelector() {
    const langBtn = document.getElementById('lang-btn');
    const langDropdown = document.getElementById('lang-dropdown');
    const currentLang = document.getElementById('current-lang');

    if (!langBtn || !langDropdown || !currentLang) return;

    // Set initial language
    let currentLanguage = localStorage.getItem('language') || 'en';

    const langMap = {
        en: 'English',
        nl: 'Nederlands',
        de: 'Deutsch'
    };

    currentLang.textContent = langMap[currentLanguage] || 'English';

    // Toggle dropdown
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!langDropdown.contains(e.target) && !langBtn.contains(e.target)) {
            langDropdown.classList.remove('show');
        }
    });

    // Language selection
langDropdown.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();
        
        const lang = e.target.dataset.lang;
        localStorage.setItem('language', lang);

        if (lang === 'en') {
            currentLang.textContent = 'English';
        } else if (lang === 'nl') {
            currentLang.textContent = 'Nederlands';
        } else if (lang === 'de') {
            currentLang.textContent = 'Deutsch';
        } else if (lang === 'fr') {
            currentLang.textContent = 'Fran√ßais';
        }

        langDropdown.classList.remove('show');
        updatePageLanguage(lang);
    });
});

}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    initializeLanguageSelector();
    // Initialize with stored language
    const storedLang = localStorage.getItem('language') || 'en';
    updatePageLanguage(storedLang);

    // Initialize feather icons after language update
    if (window.feather) {
        feather.replace();
    }
});
